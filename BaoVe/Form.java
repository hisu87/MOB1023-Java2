    List<SinhVien> list  = new ArrayList();
    DefaultTableModel tableModel;
    int index = -1 ;
    public void initTable(){
        tableModel  =new DefaultTableModel();
        Object[] cols = new Object[]{"Tên","Điểm"};
        tableModel.setColumnIdentifiers(cols);
        Table.setModel(tableModel);
    }
    public void fillTable(){
        while(tableModel.getRowCount() > 0){
            tableModel.removeRow(0);
        }
        for(SinhVien sv : list){
            tableModel.addRow(new Object[]{sv.getTen(),String.valueOf(sv.getDiem())});
        }
    }
    
    public void fillSinhVienLenForm(SinhVien sv){
        txtName.setText(sv.getTen());
        txtDiem.setText(String.valueOf(sv.getDiem()));
    }
    public void fillSinhVienlenForm(int index){
        txtName.setText(list.get(index).getTen());
        txtDiem.setText(String.valueOf(list.get(index).getDiem()));
    }
    public SinhVien ThemForm(){
            return new SinhVien(txtName.getText(),Float.valueOf(txtDiem.getText()));
    }
    public void newForm(){
        txtName.setText("");
        txtDiem.setText("");
    }
    public void Xoa(){
        int index = Table.getSelectedRow();
        list.remove(index);
        fillTable();
        newForm();
    }
    public void Sua(){
        int index = Table.getSelectedRow();
        SinhVien sv = list.get(index);
        
        sv.setTen(txtName.getText());
        sv.setDiem(Float.valueOf(txtDiem.getText()));
    }
    public void UpdateInfor(){
        Table.setRowSelectionInterval(index, index);
        fillSinhVienlenForm(index);
    }
    public void First(){
        index = 0;
        UpdateInfor();
    }
    public void next(){
        if(index == list.size() -1 ){
            First();
        }else{
            index ++;
        }
        
        UpdateInfor();
    }
    
    public void back(){
        if(index == 0) {
            last();
        }else{
            index--;
        }
        
        UpdateInfor();
    }
    public void last(){
        index = list.size() - 1;
        UpdateInfor();
    }
    public void saveFile(){
        try {
            XFile.writeObject("C:\\FPT Polytechnic\\JAVA2_CoHa\\Labs\\test2.txt", list);
            JOptionPane.showMessageDialog(this, "Save");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    public void openFile() {
        list = (List<SinhVien>) XFile.readObject("C:\\FPT Polytechnic\\JAVA2_CoHa\\Labs\\test2.txt");
        fillTable();
    }

